    subjCode = 'Subject1';
elseif strcmp(subjID,'BMC')
    subjCode = 'Subject2';
elseif strcmp(subjID,'KAS')
    subjCode = 'Subject3';
end

%% Load cached data
load(fullfile(paramsCacheFolder,'tracking',[subjCode '_paramsCache.mat']));

%% Make the tracking task packet
lagVec = lagsMat(:)';
