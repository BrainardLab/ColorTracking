function thePacketTrack = makeTrackingPacket(subjID)
% This function loads the data and returns the packet for the tracking task.

%% Set up subjecct info
projectName = 'ColorTracking';
paramsCacheFolder = getpref(projectName,'paramsCacheFolder');

% get subject code
